package 문제풀이;

import java.util.Iterator;

public class 성적확인문제 {

	public static void main(String[] args) {

		String[] subject = { "컴퓨터", "국어", "수학", "영어", "체육" };
		int[] term1 = { 77, 88, 99, 80, 70 };
		int[] term2 = { 99, 100, 88, 80, 70 };
		int a = 0;
		int b = 0;
		int  location = -1;
		// 1. 성적이 2학기에 향상된 과목의 수를 카운트해보세요.
		for (int i = 0; i < term2.length; i++) {
			if (term1[i] < term2[i]) {
				a++;
			}
		}
		System.out.println("2학기에 향상된 과목의 수는 " + a + "과목");
		// 2. 성적이 1,2학기 동일한 과목의 수를 카운트해보세요.
		for (int i = 0; i < term2.length; i++) {
			if (term1[i] == term2[i]) {
				b++;
				location = i;
				
			}
		}
		System.out.println("1,2학기 동일한 과목의 수는 " + b + "과목");

		// 3. 성적이 1,2학기 동일한 과목명을 프린트해보세요.
		if (location != -1) {
			System.out.println("성적이 1,2학기 동일한 과목은 " + subject[location]);
		}else {
			System.out.println("동일한 과목이 없습니다.");
		}

		
		
		
		
		
		
		
		
		
		
		
		
	}

}
